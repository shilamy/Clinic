    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        int PatientId = Integer.parseInt(patientid.getText());
          try {
    // Query to fetch patient information
    String patientQuery = "SELECT * FROM patienttbl WHERE PatientId = ?";
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinicdb","root","");

    PreparedStatement ptst = con.prepareStatement(patientQuery);
    ptst.setInt(1, PatientId);
    ResultSet patientRs = ptst.executeQuery();

    // Checking if patient information is found
    if (patientRs.next()) {
        // Displaying the patient information
        int ageValue = patientRs.getInt("Age");
        String genderValue = patientRs.getString("Gender");
        float weightValue = patientRs.getFloat("Weight");
        float heightValue = patientRs.getFloat("Height");
        String lifestyleData = patientRs.getString("Lifestyle");

        // Processing lifestyle choices
        boolean smokingValue = false;
        boolean alcoholValue = false;
        boolean exercisefitnessValue = false;
        if (lifestyleData != null) {
            String[] lifestyleChoices = lifestyleData.split(",");
            for (String choice : lifestyleChoices) {
                if (choice.trim().equalsIgnoreCase("Smoking")) {
                    smokingValue = true;
                } else if (choice.trim().equalsIgnoreCase("Alcohol")) {
                    alcoholValue = true;
                } else if (choice.trim().equalsIgnoreCase("ExerciseFitness")) {
                    exercisefitnessValue = true;
                }
            }
        }

        // Setting patient information to GUI components
        age.setText(String.valueOf(ageValue));
        gender.setText(genderValue);
        weight.setText(String.valueOf(weightValue));
        height.setText(String.valueOf(heightValue));
        smoking.setSelected(smokingValue);
        alcohol.setSelected(alcoholValue);
        exercisefitness.setSelected(exercisefitnessValue);
    } else {
        // Displaying a message if patient not found
        JOptionPane.showMessageDialog(null, "Patient not found.");
    }

    // Query to fetch medical condition details
    String medicalQuery = "SELECT * FROM medicalconditiontbl WHERE patientid = ?";
    PreparedStatement medicalStmt = con.prepareStatement(medicalQuery);
    medicalStmt.setInt(1, PatientId);
    ResultSet medicalRs = medicalStmt.executeQuery();

    // Checking if medical condition details are found
    if (medicalRs.next()) {
        // Displaying the medical condition information
        float bloodPressureSytolicValue = medicalRs.getFloat("BloodPressureSytolic");
        float bloodPressureDiastolicValue = medicalRs.getFloat("BloodPressureDiastolic");
        float heartRateValue = medicalRs.getFloat("HeartRate");
        boolean hypertensionValue = medicalRs.getBoolean("Hypertension");
        float cholesterolLevelValue = medicalRs.getFloat("CholesteralLevel");
        String ecgResultsValue = medicalRs.getString("ECGResults");

        // Setting medical condition information to GUI components
        bpsytolic.setText(String.valueOf(bloodPressureSytolicValue));
        bpdiastolic.setText(String.valueOf(bloodPressureDiastolicValue));
        bpm.setText(String.valueOf(heartRateValue));
        hypertension.setSelectedItem(hypertensionValue ? "Yes" : "No");
        cholesterallevel.setText(String.valueOf(cholesterolLevelValue));
        ecgresults.setText(ecgResultsValue);
    } else {
        // Displaying a message if medical condition details not found
        JOptionPane.showMessageDialog(null, "Medical condition details not found.");
    }
    
        } catch (SQLException e) {
    // Handle SQL exceptions
    e.printStackTrace();
        
         }

    }//GEN-LAST:event_searchActionPerformed

