    private void submitriskbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitriskbtnActionPerformed
int ageValue = Integer.parseInt(age.getText());
    boolean smokingValue = smoking.isSelected();
    boolean alcoholValue = alcohol.isSelected();
    boolean exerciseFitnessValue = exercisefitness.isSelected();
    String selectedHypertension = (String) hypertension.getSelectedItem();
    boolean hypertensionValue = selectedHypertension.equals("Yes");
    float cholesterolLevelValue = Float.parseFloat(cholesterallevel.getText());
    float bloodPressureSystolicValue = Float.parseFloat(bpsytolic.getText());
    float bloodPressureDiastolicValue = Float.parseFloat(bpdiastolic.getText());
    float heartRateValue = Float.parseFloat(bpm.getText());

    // Risk score calculation and display...
    // Define weights for each factor
    final float AGE_WEIGHT = 0.3f;
    final float LIFESTYLE_WEIGHT = 0.4f;
    final float MEDICAL_CONDITION_WEIGHT = 0.3f;

    // Initialize variables to store calculated factors
    float ageFactor = 0;
    float lifestyleFactor = 0;
    float medicalConditionFactor = 0;

    // Age factor calculation
    // Example: Older age contributes to higher risk
    if (ageValue >= 50) {
        ageFactor = 0.5f;
    } else if (ageValue >= 40) {
        ageFactor = 0.3f;
    } else {
        ageFactor = 0.1f;
    }

    // Lifestyle factor calculation
    // Example: Smoking and alcohol consumption increase risk
    if (smokingValue) {
        lifestyleFactor += 0.2f;
    }
    if (alcoholValue) {
        lifestyleFactor += 0.1f;
    }
    if (exerciseFitnessValue) {
        lifestyleFactor -= 0.1f; // Exercise reduces risk
    }

    // Medical condition factor calculation
    // Example: High blood pressure, cholesterol, and heart rate increase risk
    if (hypertensionValue) {
        medicalConditionFactor += 0.2f;
    }
    if (cholesterolLevelValue >= 200) {
        medicalConditionFactor += 0.1f;
    }
    if (bloodPressureSystolicValue >= 140 || bloodPressureDiastolicValue >= 90) {
        medicalConditionFactor += 0.1f;
    }
    if (heartRateValue >= 100) {
        medicalConditionFactor += 0.1f;
    }

    // Calculate overall risk score
    float riskScore = (AGE_WEIGHT * ageFactor) + (LIFESTYLE_WEIGHT * lifestyleFactor) + (MEDICAL_CONDITION_WEIGHT * medicalConditionFactor);
    // Categorize the risk based on thresholds
    String riskCategory;
    if (riskScore >= 0.7) {
        riskCategory = "High";
    } else if (riskScore >= 0.4) {
        riskCategory = "Moderate";
    } else {
        riskCategory = "Low";
    }
    // Display risk score to the user
     JOptionPane.showMessageDialog(null, "Risk Score: " + riskScore + "\nRisk Category: " + riskCategory);
     


    }//GEN-LAST:event_submitriskbtnActionPerformed

